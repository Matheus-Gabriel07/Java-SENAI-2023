package Aula06.FOR;

public class PareseImpares {
    public static void main(String[] args) {
        for (int i = 0; i<=10; i++) {
            if (i%2==0){
                System.out.println(i+" é um número é par");
            }else{
                System.out.println(i+" é um número é impar");
            }
        }
    }
}
