package Aula06;

/*Laços de repetição são utilizados para executar um bloco de código uma determinada quantidade
 *de vezes, ou até que uma condição seja satisfeita.

    No java temos 4 laços de repetição: FOR, WHILE, DO WHILE

    1) FOR --> PARA
    Usado para executar o código determinada quantidade de vezes.


*/

public class Laco{
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println("Valor de I: "+i);
        }
    }
}